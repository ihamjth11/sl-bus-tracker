export function searchLocations(query) {
  if (!query || query.length < 2) return [];
  
  const q = query.toLowerCase().trim();
  
  const results = sriLankaLocations.filter(loc => {
    // Direct name match
    if (loc.name.toLowerCase().includes(q)) return true;
    // Alias match
    if (loc.aliases.some(alias => alias.includes(q))) return true;
    // District match
    if (loc.district.toLowerCase().includes(q)) return true;
    // Typo correction - fuzzy match
    if (fuzzyMatch(q, loc.name.toLowerCase())) return true;
    return false;
  });

  return results.slice(0, 8);
}

function fuzzyMatch(query, target) {
  if (Math.abs(query.length - target.length) > 3) return false;
  let mismatches = 0;
  const minLen = Math.min(query.length, target.length);
  for (let i = 0; i < minLen; i++) {
    if (query[i] !== target[i]) mismatches++;
    if (mismatches > 2) return false;
  }
  return mismatches <= 2;
}